void main(){
  int a=5,b=10;  //arithmetic operator

  print("Addition ${a+b}");
  print("Sub ${a-b}");
  print("Multiplication ${a*b}");
  print("Division ${a/b}");
  print("Reminder ${a%b}");

// relational operator

  print("a is equal to b :${a==b}");
  print("a is not equal to b: ${a!=b}");
  print("a is greater than  b: ${a>b}");
  print("a is less b: ${a<b}");

  // logical operator

  bool x= true,y=false;

  print("x and y ${x && y}");
  print("x or y ${x || y}");


  int v=10;
  int  c=-v;

  v++;
  c--;
}